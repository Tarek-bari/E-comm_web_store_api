import { Schema, model } from 'mongoose'

const orderSchema = new Schema({
    productId: {
        type: String,
        required: true
    },
    customerId: {
        type: String,
        required: true
    },
    productName: {
        type: String,
        required: true
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    color: {
        type: String,
        required: true
    },
    size: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    deliveryPrice: {
        type: Number,
        required: true
    },
    status: {
        type: String,
        enum: ['Pending', 'Processing', 'Shipped', 'Delivered', 'Canceled', 'Returned'],
        default: 'Pending'
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
})

export default model('Order', orderSchema)